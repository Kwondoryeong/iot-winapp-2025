using System.Drawing.Text;
using System.Runtime.InteropServices;

namespace SyntaxWinApp01
{
    public partial class FrmMain : Form
    {
        [DllImport("kernel32.dll")]
        private static extern bool AllocConsole(); // 콘솔 창 열기 위한 선언

        // 대리자(delegate) 선언
        delegate void MyDelegate(string msg);

        // 이벤트 핸들러 대리자 선언
        delegate void MyEventHandler(object sender, EventArgs e);
        // 이벤트 선언
        public event EventHandler SomethingHappend;

        // 대리자에서 호출할 메서드 - 대리자와 파라미터가 일치
        void SayHello(string msg)
        {
            MessageBox.Show($"안녕, {msg}", "메시지",
                MessageBoxButtons.OK, MessageBoxIcon.Information);
        }
        void SayGoodbye(string msg)
        {
            MessageBox.Show($"내일봐~, {msg}", "메시지",
                MessageBoxButtons.OK, MessageBoxIcon.Information);
        }
        public FrmMain()
        {
            InitializeComponent();
            AllocConsole();
            Console.WriteLine("1. 폼 생성자 실행");
            TxtLog.Text += ("1. 폼 생성자 실행\r\n");
        }
        private void FrmMain_Load(object sender, EventArgs e) {
            Console.WriteLine("2. 폼로드 이벤트 실행");
            TxtLog.Text += ("2. 폼로드 이벤트 실행\r\n");
        }
        private void FrmMain_Activated(object sender, EventArgs e) {
            Console.WriteLine("3. 폼액티베이티드 이벤트 실행");
            TxtLog.Text += ("3. 폼액티베이티드 이벤트 실행\r\n");
        }
        private void FrmMain_Shown(object sender, EventArgs e) {
            Console.WriteLine("4. 폼숀 이벤트 실행");
            TxtLog.Text += ("4. 폼숀 이벤트 실행\r\n");

        }
        private void FrmMain_FormClosing(object sender, FormClosingEventArgs e) {
            Console.WriteLine("5. 폼클로징 이벤트 실행");
            TxtLog.Text += ("5. 폼클로징 이벤트 실행\r\n");
        }

       private void FrmMain_FormClosed(object sender, FormClosedEventArgs e){
            Console.WriteLine("6. 폼클로즈드 이벤트 실행");
            TxtLog.Text += ("6. 폼클로즈드 이벤트 실행\r\n");
       }





        private void BtnCheck_Click(object sender, EventArgs e)
        {
            // 1. 직접 호출
            SayHello("DoHyeong");
            SayGoodbye("DoHyeong");

            // 2. 대리자(delegate) 호출(윈앱 개발 핵심!!)
            MyDelegate del = SayHello; // 대리자에 호출할 메서드를 묶어줌
            del += SayGoodbye;          // 추가도 가능 (대리자 체인(함수) 추가)
            del -= SayHello;            // 대리자 체인(함수) 삭제
            del("TaeHyeong");           // 등록된 함수 출력

            // 3. 변환값 없는 대리자 - Action
            Action<string> act = SayHello;
            act("Chris");

            // 4. 반환값 있는 대리자 - Func
            Func<int, int, int> add = (a, b) => a + b;
            int result = add(7, 8);
            Console.Write(result);

            // 이벤트 실행
            if (SomethingHappend != null)
            {
                SomethingHappend(this, new EventArgs()); // 이벤트 실행
            }

        }



    }
}
